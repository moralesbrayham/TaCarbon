private void cargarRoles() {
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/tacarbon_db", "root", "m1o3r5a7");
        String sql = "SHOW COLUMNS FROM usuarios LIKE 'rol'";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            String enumValues = rs.getString("Type"); // Get ENUM definition
            enumValues = enumValues.replaceAll("enum\\(|\\)", ""); // Remove "enum(" and ")"
            String[] roles = enumValues.replace("'", "").split(","); // Remove quotes and split values

            cbRol.removeAllItems(); // Clear existing values
            for (String rol : roles) {
                cbRol.addItem(rol); // Add each ENUM value to JComboBox
            }
        }

        stmt.close();
        conn.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar roles: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

    
private void cargarUsuarios() {
    DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
    model.setRowCount(0); // Clear table

    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/tacarbon_db", "root", "m1o3r5a7");
        String sql = "SELECT * FROM usuarios";
        PreparedStatement stmt = conn.prepareStatement(sql);
        ResultSet rs = stmt.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nombre"),
                rs.getString("correo"),
                rs.getString("rol")
            });
        }

        stmt.close();
        conn.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar usuarios: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


